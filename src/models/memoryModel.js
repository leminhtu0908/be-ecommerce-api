const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const memoryModel = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
    products: [
      {
        type: Schema.Types.ObjectId,
        ref: "Product",
      },
    ],
  },
  {
    timestamps: true,
  }
);
const Memory = mongoose.model("Memory", memoryModel);
module.exports = Memory;
